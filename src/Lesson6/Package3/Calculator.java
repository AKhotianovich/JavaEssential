package Lesson6.Package3;

public class Calculator {
    double sum(double a, double b){
        return a + b;
    }

    double raz(double a, double b){
        return a - b;
    }

    double umn(double a, double b){
        return a * b;
    }

    double del(double a, double b){
        return a / b;
    }

}
